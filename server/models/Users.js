import mongoose from 'mongoose';

const UserSchema = mongoose.Schema (
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      unique: true,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    profilePicture: {
      type: String,
    },
  },
  {
    timestamps: true,
  }
);

const Users = mongoose.model ('Users', UserSchema);
export default Users;
